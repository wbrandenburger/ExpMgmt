import re

#   class -------------------------------------------------------------------
# ---------------------------------------------------------------------------
class ReSearch():

    #   method --------------------------------------------------------------
    # -----------------------------------------------------------------------
    def __init__(self, regex, group):
        self._regex = re.compile(regex)
        self._group = int(group)

    #   method --------------------------------------------------------------
    # -----------------------------------------------------------------------   
    def __call__(self, pattern):
        try:
            result = self._regex.search(pattern).group(self._group)
        except AttributeError:
            result = None
        return result

